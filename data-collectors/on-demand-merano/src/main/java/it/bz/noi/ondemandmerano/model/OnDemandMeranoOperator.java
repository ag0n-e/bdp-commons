package it.bz.noi.ondemandmerano.model;

public class OnDemandMeranoOperator {

    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "OnDemandMeranoStopRegion{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
